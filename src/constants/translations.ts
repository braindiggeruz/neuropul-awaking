export const getTranslation = (key: string, lang: 'ru' | 'uz'): string => {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  if (typeof value === 'string') {
    return value;
  }
  
  // Fallback to Russian if translation not found
  if (lang === 'uz') {
    value = translations['ru'];
    for (const k of keys) {
      value = value?.[k];
    }
    
    if (typeof value === 'string') {
      return value;
    }
  }
  
  return key;
};